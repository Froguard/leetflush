var twoEggDrop = function (n) {
  let res = record.get(n);
  if (res === undefined) {
    if (n === 0) {
      // N 楼层为 0 的意义，鸡蛋本身就在地上了，没碎，不需要尝试，即 0 次尝试
      res = 0;
    } else if (n === 1) {
      res = 1;
    } else {
      // TODO: 以下主核心逻辑，能实现，但我没看懂！
      let s = 0;
      res = 0;
      for (let i = 1; s < n; i++) {
        s += i;
        res = i;
      }
    }
    record.set(n, res);
  }
  return res;
};

